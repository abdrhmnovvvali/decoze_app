import 'package:decoze_app/presentation/controllers/complete_profile_controller.dart';
import 'package:flutter/material.dart';

import '../../../../widgets/custom_profile_photo.dart';

class ProfileDrag extends StatelessWidget {
  const ProfileDrag({super.key});

  @override
  Widget build(BuildContext context) {
    final completeController = CompleteProfileController.instance;
    return ValueListenableBuilder(
      valueListenable: completeController.profileImageController,
      builder: (_, fileImage, _) {
        return CustomProfilePhoto(addEdit: true, showEdit: true, radius: 72);
      },
    );
  }
}
